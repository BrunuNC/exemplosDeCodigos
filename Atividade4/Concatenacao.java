package Atividade4;

public class Concatenacao {
    public static void main(String[] args) {
        String elem1 = "Sistemas";       //Armazena as Strings nas variaveis
        String elem2 = " de informação";
        String computacao = (elem1 + elem2); //armazena a concatenação de instrings das variáveis "elem1" e "elem2" na variável "computacao"
        System.out.println(computacao); //Imprime concatenando o valor da variável "computacao"
    }
}
